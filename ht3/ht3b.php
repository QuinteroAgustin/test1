    <h1>ht3b : recette du Mojito</h1>
    
    <img src="img/mojito.jpg" alt="Mojito">

    <h2>Ingrédients (2 personnes)</h2>
    <ul>
        <li>4 cl de rhum blanc</li>
        <li>2 cl de sirop de sucre de canne</li>
        <li>6 à 8 feuilles de menthe</li>
        <li>1 demi citron vert</li>
        <li>Un peu d'eau gazeuse</li>
        <li>10 glaçons</li>
    </ul>
    
    <h2>Matériel</h2>
    <ul>
        <li>Couteau d'office</li>
        <li>Rouleau à pâtisserie</li>
        <li>Pilon à cocktail</li>
        <li>Torchon</li>
    </ul>

    <h2>Recette</h2>
    <ol>
        <li>Mettez vos glaçons dans un torchon, refermez-le puis, à l'aide d'un rouleau à pâtisserie, pilez la glace</li>
        <li>Vous pouvez encore avoir des morceaux. Versez dans un bol et réservez au congélateur</li>
        <li>Versez les feuilles de menthe dans chaque verre</li>
        <li>Coupez le citron en deux puis chaque demi citron en 6 morceaux</li>
        <li>Ajoutez les 6 morceaux de citron dans chaque verre (1/2 citron)</li>
        <li>Ajoutez le sirop de sucre de canne</li>
        <li>Ecrasez le citron avec un pilon spécial cocktail</li>
        <li>Ajoutez la glace pilée en laissant 2 cm de libre. Plus il y a de glace, moins elle fondra rapidement</li>
        <li>Ajoutez le rhum</li>
        <li>Complétez avec l'eau gazeuse</li>
        <li>Mélangez le cocktail afin que les saveur se mêlent</li>
        <li>Servir le mojito avec deux pailles qui vont permettre de mélanger le cocktail au fur et à mesure de la dégustation</li>
    </ol>

