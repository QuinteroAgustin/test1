    <h1>ht13 : tortues Ninja</h1>
    <h3>Leonardo</h3>
    <p>Leader des 4 frères, vivant sous les principes du bushidō. Il prend toujours au sérieux la pratique du ninjutsu et tente d'encourager ses frères à faire de même, souvent en vain. Son nom vient de l’artiste et ingénieur de la renaissance Leonardo da Vinci. Son caractère évolue très peu à travers les différentes versions depuis 1984.</p>
    <p>Il porte un bandeau bleu et utilise deux Ninjatōs pour arme.</p>
    <h3>Raphael</h3>
    <p>C'est la tête brûlée des quatre frères. Bien qu'il s'oppose souvent aux ordres de Leonardo, ce qui leur vaut de très nombreuses disputes, il est toujours présent pour aider ses frères. Son nom vient également d'un artiste de la Renaissance italienne, Raphaël. Ce fut la tortue qui connut le plus rude changement avec le passage à l’écran, dans le premier dessin animé, en 1987. Étant d’un tempérament violent à l’origine, il devient une tortue aimant l’humour.</p>
    <p>Il porte un bandeau rouge et utilise deux sais pour arme.</p>
    <h3>Donatello</h3>
    <p>C'est le plus pacifique, il préfère de loin l'utilisation de la parole à celle de son bō pour régler les conflits. Intellectuel et amateur de science, il est capable de modifier, réparer et comprend la plupart des technologies. Son nom est inspiré du peintre et sculpteur florentin Donatello. Dans le dessin animé de 2012, il est fou amoureux d’April O’Neil.</p>
    <p>Il porte un bandeau violet et utilise un bō pour arme.</p>
    <h3>Michelangelo</h3>
    <p>Le plus jeune de la famille, il aime bien plaisanter, les bandes dessinées faire du skateboard, du surf et réputé comme grand mangeur de pizza et un peu fainéant, mais bon cœur et frère aimable.Sous son attitude enfantine se cache un ninja agile et redoutable. Son nom vient de l'artiste et ingénieur italien de la Renaissance Michel-Ange. Dans le dessin animé de 2012, il donne le surnom de la plupart des personnages secondaires rencontrés au cours de l’histoire.</p>
    <p>Il porte un bandeau orange et utilise généralement deux nunchakus pour armes, et des tonpha dans la série de 1997. </p>
