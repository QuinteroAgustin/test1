        <h1>Services proposés</h1>
        <p>Q. Pourquoi l’hébergement de comités départementaux perdure-t-il ?</p>
        <p>R. Lors de la reprise de la Maison des Ligues en 2000 par la Région Lorraine et la négociation qui en a suivi, le mouvement sportif a défendu la présence des comités départementaux dans les locaux existants. Cette présence est remise en question régulièrement, mais il est probable que rien ne sera fait avant la réforme des territoires et la « fusion » des conseillers régionaux et généraux en conseillers territoriaux.</p>
        <p>Interview de son directeur, M. Lucien Sapin.</p>
        <p>Q. Quelles sont les structures que vous hébergez ?</p>
        <p>R. La Maison héberge la majorité des ligues sportives régionales, de la ligue de tennis (la plus grosse, qui emploie 8 personnes) à des ligues de sports qui n’ont pas d’employés permanents, comme le bowling ou la plongée sous-marine. La ligue de football occupe 2000 m2 de bureaux dans la banlieue de Nancy et ne sera probablement jamais hébergée dans nos locaux. Nous hébergeons également, comme je vous le disais, quelques comités départementaux, ainsi que le CROSL et sa déclinaison départementale : le CDOS (Comité Départemental Olympique et Sportif).</p>
        <p>Interview de son directeur, M. Lucien Sapin.</p>
        <p>Q. Existe-il une convention d’hébergement ?</p>
        <p>R. Oui, la convention initiale date de 2003. On parle de convention d’occupation du domaine public. Elle est en cours de révision, puisque les locaux ont été agrandis et que de nouveaux services sont apparus. La nouvelle version sera disponible l’été prochain, elle est assortie d’un règlement intérieur. Dans cette convention figure notamment l’obligation d’assurances en responsabilité civile des structures hébergées. Je peux vous donner un exemple de cette convention.</p>
        <p>Interview de son directeur, M. Lucien Sapin.</p>
        <p>Q. Comment fonctionne le système de réservation des salles ressources, comme les salles de réunions, l’amphithéâtre ou encore la nouvelle salle de convivialité ?</p>
        <p>R. Concernant l’amphi, il y a 1⁄2 journée gratuite par an pour chaque ligue et comité départemental (CD). Les salles de réunions d’étage sont mises librement à la disposition des occupants de l’étage. Les salles de réunions sont réservables via l’Intranet, avec différents services associés dont certains sont payants. Il existe quatre niveaux de tarification. En 2009, nous avons reçu 2732 réunions et 47316 personnes ont assisté à ces réunions.</p>
        <p>Interview de son directeur, M. Lucien Sapin.</p>
